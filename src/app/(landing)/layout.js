import Navbar from "@/components/header/navbar";
import { Inter, Flow_Circular } from "next/font/google";
import "../globals.css";

import { Providers } from "@/store/Provider";
import ClientBottomPlayer from "@/components/music/client-bottom-player";
import { PopulateStore } from "@/store/PopulateStore";
import { ClientMediaElementProviders } from "@/contexts/ClientMediaElementContext";
import BottomFooter from "@/components/footer/bottom";
import CopyWrite from "@/components/footer/copywrite";
import MobileNavbar from "@/components/header/mobile-navbar";
import store from "@/store/store";
import { profile } from "@/services/user.service";
import { getTokenSSR, deleteCookie } from "../actions/auth";

const inter = Inter({ subsets: ["latin"] });

export const metadata = {
  title: "",
  description: "Generated by create next app",
};

export default async function RootLayout({ children }) {
  var userProfile = null;
  const authToken = getTokenSSR();
  if (authToken) {
    await profile({}, authToken).then((data) => {
      userProfile = data;
    });
  }

  return (
    <html lang="en">
      <body className={(inter.className, "bg-primaryBg")}>
        <Providers store={store}>
          <PopulateStore userProfile={userProfile}>
            <ClientMediaElementProviders>
              <Navbar className="hidden md:block" />
              <MobileNavbar className="block md:hidden" />
              {children}
              <ClientBottomPlayer></ClientBottomPlayer>
              <div className="border-t border-tertiaryBg mt-[93px]">
                <BottomFooter></BottomFooter>
                <CopyWrite></CopyWrite>
              </div>
            </ClientMediaElementProviders>
          </PopulateStore>
        </Providers>
      </body>
    </html>
  );
}
